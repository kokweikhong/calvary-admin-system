import { z } from "zod";

export const InventoryProductSchema = z.object({
  id: z.number(),
  code: z.string(),
  name: z.string(),
  brand: z.string(),
  standardUnit: z.string(),
  thumbnail: z.string(),
  supplier: z.string(),
  remarks: z.string(),
  isExist: z.boolean(),
  createdBy: z.string(),
  createdAt: z.string(),
  updatedBy: z.string(),
  updatedAt: z.string(),
});

export type InventoryProduct = z.infer<typeof InventoryProductSchema>;

// extend interface
export const InventoryProductSummarySchema = InventoryProductSchema.extend({
  totalIncoming: z.number(),
  totalOutgoing: z.number(),
  totalBalance: z.number(),
});

export type InventoryProductSummary = z.infer<
  typeof InventoryProductSummarySchema
>;

export const InventoryIncomingSchema = z.object({
  id: z.number(),
  productId: z.number(),
  status: z.string(),
  quantity: z.number(),
  length: z.number(),
  width: z.number(),
  height: z.number(),
  unit: z.string(),
  standardQuantity: z.number(),
  refNo: z.string(),
  refDoc: z.string(),
  cost: z.number(),
  storeLocation: z.string(),
  storeCountry: z.string(),
  remarks: z.string(),
  createdBy: z.string(),
  createdAt: z.string(),
  updatedBy: z.string(),
  updatedAt: z.string(),

  productCode: z.string(),
  productName: z.string(),
  standardUnit: z.string(),
});

export type InventoryIncoming = z.infer<typeof InventoryIncomingSchema>;

export const InventoryOutgoingSchema = z.object({
  id: z.number(),
  incomingId: z.number(),
  productId: z.number(),
  status: z.string(),
  quantity: z.number(),
  standardQuantity: z.number(),
  cost: z.number(),
  refNo: z.string(),
  refDoc: z.string(),
  remarks: z.string(),
  createdBy: z.string(),
  createdAt: z.string(),
  updatedBy: z.string(),
  updatedAt: z.string(),

  productCode: z.string(),
  productName: z.string(),
  standardUnit: z.string(),

  incomingBalance: z.number(),
});

export type InventoryOutgoing = z.infer<typeof InventoryOutgoingSchema>;

