import { z } from "zod";

export const InventoryProductSchema = z.object({
  id: z.number(),
  code: z.string(),
  name: z.string(),
  brand: z.string(),
  standardUnit: z.string(),
  thumbnail: z.string(),
  supplier: z.string(),
  remarks: z.string(),
  isExist: z.boolean(),
  createdBy: z.string(),
  createdAt: z.string(),
  updatedBy: z.string(),
  updatedAt: z.string(),
});

export type InventoryProduct = z.infer<typeof InventoryProductSchema>;

